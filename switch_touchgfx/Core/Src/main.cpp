void StartUITask(void const *argument)
{
  sunMsgBox_g = osMailCreate(osMailQ(sunMsgBox_g), NULL);
  main_screen_state_t state = {0};
  for (;;)
  {
    state.sunState++;
    state.hF++;
    state.dayOfWeek++;
    state.hour++;
    state.minute++;
    
    if (state.sunState >= 9)
      state.sunState = 0;
    if (state.hF)
      state.hF = 0;
    if (state.dayOfWeek >= 7)
      state.dayOfWeek = 0;
    if (state.hour >= 12)
      state.hour = 0;
    if (state.minute >= 60)
      state.minute = 0;
    osDelay(1000);
    putSunMsg(state);
  }
}

